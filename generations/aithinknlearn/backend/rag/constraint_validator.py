    def reject_non_compliant(
        self,
        generated_output: str,
        constraints: Dict[str, Any]
    ) -> Dict[str, Any]:
        """
        Reject non-compliant outputs before they reach the user

        Args:
            generated_output: Output from LLM
            constraints: Validation constraints

        Returns:
            Dictionary with:
                - accepted: Boolean indicating if output is accepted
                - validation_results: Full validation results
                - rejection_reason: Reason for rejection (if rejected)
        """
        logger.info("Checking if output should be rejected")

        validation_results = self.validate_output(generated_output, constraints)

        if validation_results["is_valid"]:
            logger.info("Output ACCEPTED - passes all validations")
            return {
                "accepted": True,
                "validation_results": validation_results,
                "rejection_reason": None
            }
        else:
            total_violations = validation_results["summary"]["total_violations"]
            logger.warning(f"Output REJECTED - {total_violations} violations found")

            # Build rejection reason
            reasons = []
            for val_name, val_result in validation_results["validations"].items():
                if not val_result.get("is_valid", False):
                    reasons.append(
                        f"{val_name}: {val_result.get('violations_count', 0)} violations"
                    )

            rejection_reason = "; ".join(reasons)

            return {
                "accepted": False,
                "validation_results": validation_results,
                "rejection_reason": rejection_reason
            }
